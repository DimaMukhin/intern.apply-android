package intern.apply.internapply.model;

public class Comment {

    private int id;
    private int jobID;
    private String message;
    private String author;
    private String ts;

    public int getId() {
        return id;
    }

    public int getJobID() {
        return jobID;
    }

    public String getMessage() {
        return message;
    }

    public String getAuthor() {
        return author;
    }

    public String getTs() {
        return ts;
    }
}
